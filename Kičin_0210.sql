select nazivpro,sum(kolicinaok) komada
from evidnormi('2017-01-31','2017-10-31',0)
where NAZIVPRO LIKE '%Y 507%' OR NAZIVPRO LIKE '%SY 507%'
OR 
NAZIVPRO LIKE '%YJ 504%' OR NAZIVPRO LIKE '%SYF 504%'
OR 
NAZIVPRO LIKE '%YJ 505%' OR NAZIVPRO LIKE '%SYF 505%'
OR 
NAZIVPRO LIKE '%YJ 506%' OR NAZIVPRO LIKE '%SYF 506%'
OR 
NAZIVPRO LIKE '%Y 508%' OR NAZIVPRO LIKE '%F 508%' OR NAZIVPRO LIKE '%J 508%'
OR 
NAZIVPRO LIKE '%Y 509%' OR NAZIVPRO LIKE '%F 509%' 
OR 
NAZIVPRO LIKE '%Y 510%' OR NAZIVPRO LIKE '%F 510%' 
OR 
NAZIVPRO LIKE '%Y 511%' OR NAZIVPRO LIKE '%F 511%' 
OR 
NAZIVPRO LIKE '%Y 513%' 
group by nazivpro
ORDER BY NAZIVPRO
